# 作用：比较运算符

a,b=10,20
print('a>b?',a>b)#比较运算符的结果为bool类型
print('a<b?',a<b)
print('a>=b?',a>=b)
print('a<=b?',a<=b)

print('a==b?',a==b)#'=='比较的是变量的值，不是id。一个变量有三个属性，id，value，type。比较id的是‘is’
print('a is b',a is b)
print('a is not b',a is not b)

print('a!=b?',a!=b)

