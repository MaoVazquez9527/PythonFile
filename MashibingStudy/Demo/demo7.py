# 作用：算术运算符

print(1+1)#加法运算

print(1-1)#减法运算

print(2*4)#乘法运算

print(11/2)#除法运算

print(11//2)#整除运算，即取整数部分
print(9//-4)#一正一负结果向下取整

print(11%2)#取余运算，即取余数部分
print(9%-4)#一正一负使用公式：   余数=被除数-除数*商    9-（-4）*（-3）
print(-9%4)#一正一负使用公式：                       -9-（4）*（-3）

print(2**2)#幂运算
